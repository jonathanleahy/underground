/**
 * Get booking URL for a hotel with guest details
 * Premier Inn uses a different URL structure for dates
 */
export function getBookingUrl(
  hotelId: string, 
  checkIn?: string, 
  checkOut?: string,
  adults: number = 1,
  children: number = 0
): string {
  const baseUrl = PREMIER_INN_URLS[hotelId];
  
  if (baseUrl && checkIn && checkOut) {
    // Premier Inn's actual date format in URLs
    // They use: #/book/rooms?arrivalDate=2024-12-28&departureDate=2024-12-30
    
    // Add the booking path with hash routing
    const bookingPath = `#/book/rooms?arrivalDate=${checkIn}&departureDate=${checkOut}&rooms=1&adults=${adults}&children=${children}`;
    
    // Return the full URL with booking parameters
    return baseUrl + bookingPath;
  } else if (baseUrl) {
    return baseUrl;
  } else {
    // Fallback to search page with dates
    if (checkIn && checkOut) {
      return `https://www.premierinn.com/gb/en/search.html#/book/rooms?arrivalDate=${checkIn}&departureDate=${checkOut}&adults=${adults}&children=${children}&searchTerm=London`;
    }
    return `https://www.premierinn.com/gb/en/search.html?searchTerm=London`;
  }
}